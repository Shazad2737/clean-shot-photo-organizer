class IconManager:
    """Manages SVG icon loading and rendering."""
    
    _cache: dict = {}
    
    @classmethod
    def get_icon(cls, name: str, size: int = 24, color: str = "#FFFFFF") -> QIcon:
        """
        Get a QIcon from an SVG icon name.
        
        Args:
            name: Icon name from ICONS dictionary
            size: Icon size in pixels
            color: Hex color string for the icon
            
        Returns:
            QIcon instance
        """
        cache_key = f"{name}_{size}_{color}"
        
        if cache_key in cls._cache:
            return cls._cache[cache_key]
        
        svg_data = ICONS.get(name)
        if not svg_data:
            # Return empty icon if not found
            return QIcon()
        
        # Replace stroke color
        colored_svg = svg_data.replace('stroke="currentColor"', f'stroke="{color}"')
        
        # Render SVG to pixmap
        pixmap = cls._render_svg(colored_svg, size)
        icon = QIcon(pixmap)
        
        cls._cache[cache_key] = icon
        return icon
    
    @classmethod
    def get_pixmap(cls, name: str, size: int = 24, color: str = "#FFFFFF") -> QPixmap:
        """
        Get a QPixmap from an SVG icon name.
        
        Args:
            name: Icon name from ICONS dictionary
            size: Icon size in pixels
            color: Hex color string for the icon
            
        Returns:
            QPixmap instance
        """
        svg_data = ICONS.get(name)
        if not svg_data:
            return QPixmap()
        
        colored_svg = svg_data.replace('stroke="currentColor"', f'stroke="{color}"')
        return cls._render_svg(colored_svg, size)
    
    @classmethod
    def _render_svg(cls, svg_data: str, size: int) -> QPixmap:
        """Render SVG string to QPixmap."""
        renderer = QSvgRenderer(QByteArray(svg_data.encode()))
        
        pixmap = QPixmap(size, size)
        pixmap.fill(Qt.transparent)
        
        painter = QPainter(pixmap)
        painter.setRenderHint(QPainter.Antialiasing)
        renderer.render(painter)
        painter.end()
        
        return pixmap
    
    @classmethod
    def clear_cache(cls) -> None:
        """Clear the icon cache."""
        cls._cache.clear()
    
    @classmethod
    def list_icons(cls) -> list:
        """Return list of available icon names."""
        return list(ICONS.keys())


# Convenience function for quick access
def get_icon(name: str, size: int = 24, color: str = "#FFFFFF") -> QIcon:
    """Quick access to get an icon."""
    return IconManager.get_icon(name, size, color)


def get_pixmap(name: str, size: int = 24, color: str = "#FFFFFF") -> QPixmap:
    """Quick access to get a pixmap."""
    return IconManager.get_pixmap(name, size, color)
